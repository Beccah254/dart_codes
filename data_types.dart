void main() {
  // Integer data type
  int myInt = 42;
  print('Integer: $myInt');

  // Double data type
  double myDouble = 3.14;
  print('Double: $myDouble');

  // String data type
  String myString = 'Hello, Dart!';
  print('String: $myString');

  // List data type
  List<int> myList = [1, 2, 3, 4, 5];
  print('List: $myList');

  // Map data type
  Map<String, int> myMap = {'apple': 5, 'banana': 10, 'orange': 7};
  print('Map: $myMap');

  // Testing List
  print('First element of the list: ${myList[0]}');

  // Testing Map
  print('Number of apples: ${myMap['apple']}');
}
